package at.dev.genshinbuildsaver.Objects;

public abstract class Artifact {
    int id;
    String name;
    int lvl;
    String mainStat;
    double lvlMainStat;
    String subStat1;
    double lvlSubStat1;
    String subStat2;
    double lvlSubStat2;
    String subStat3;
    double lvlSubStat3;
    String subStat4;
    double lvlSubStat4;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLvl() {
        return lvl;
    }

    public void setLvl(int lvl) {
        this.lvl = lvl;
    }

    public String getMainStat() {
        return mainStat;
    }

    public void setMainStat(String mainStat) {
        this.mainStat = mainStat;
    }

    public double getLvlMainStat() {
        return lvlMainStat;
    }

    public void setLvlMainStat(double lvlMainStat) {
        this.lvlMainStat = lvlMainStat;
    }

    public String getSubStat1() {
        return subStat1;
    }

    public void setSubStat1(String subStat1) {
        this.subStat1 = subStat1;
    }

    public double getLvlSubStat1() {
        return lvlSubStat1;
    }

    public void setLvlSubStat1(double lvlSubStat1) {
        this.lvlSubStat1 = lvlSubStat1;
    }

    public String getSubStat2() {
        return subStat2;
    }

    public void setSubStat2(String subStat2) {
        this.subStat2 = subStat2;
    }

    public double getLvlSubStat2() {
        return lvlSubStat2;
    }

    public void setLvlSubStat2(double lvlSubStat2) {
        this.lvlSubStat2 = lvlSubStat2;
    }

    public String getSubStat3() {
        return subStat3;
    }

    public void setSubStat3(String subStat3) {
        this.subStat3 = subStat3;
    }

    public double getLvlSubStat3() {
        return lvlSubStat3;
    }

    public void setLvlSubStat3(double lvlSubStat3) {
        this.lvlSubStat3 = lvlSubStat3;
    }

    public String getSubStat4() {
        return subStat4;
    }

    public void setSubStat4(String subStat4) {
        this.subStat4 = subStat4;
    }

    public double getLvlSubStat4() {
        return lvlSubStat4;
    }

    public void setLvlSubStat4(double lvlSubStat4) {
        this.lvlSubStat4 = lvlSubStat4;
    }
}
